 HAL_StatusTypeDef ELOCK_ON(void)  
{
 uint32_t  tickstart;
   LL_TIM_OC_SetCompareCH1(TIM3,200);
   HAL_TIM_PWM_Start(&htim3,TIM_CHANNEL_1);
   tickstart=HAL_GetTick();
        while(HAL_GPIO_ReadPin(iLock_FB_GPIO_Port, iLock_FB_Pin) != GPIO_PIN_SET)
        {
          if((HAL_GetTick() - tickstart ) > APP_TIMEOUT_VALUE)
          {
            return HAL_TIMEOUT;
          }
        }
     return HAL_OK;
}


